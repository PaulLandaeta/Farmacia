    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        try {
            Connection c = Conexion.obtener();
            UserService service = new UserService();
            User user = service.recuperarPorUsername(c, userNameTextField.getText());
            if (user != null && user.getPassword().equals(userPasswordField.getText())) {
                Menu menu = new Menu();
                this.setVisible(false);
                menu.setLocationRelativeTo(null);
               // menu.setExtendedState(JFrame.MAXIMIZED_BOTH);
                menu.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Contrase√±a Incorrecta", "Error", JOptionPane.ERROR_MESSAGE, null);
            }

        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

